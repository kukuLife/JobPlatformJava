package com.jobplatform.task.entity;

public class Task {

	public String task_id;
	public String task_content;
	public String task_key_world;
	public String task_answer;
	public String user_id;
	public String getTask_id() {
		return task_id;
	}
	public void setTask_id(String task_id) {
		this.task_id = task_id;
	}
	public String getTask_content() {
		return task_content;
	}
	public void setTask_content(String task_content) {
		this.task_content = task_content;
	}
	public String getTask_key_world() {
		return task_key_world;
	}
	public void setTask_key_world(String task_key_world) {
		this.task_key_world = task_key_world;
	}
	public String getTask_answer() {
		return task_answer;
	}
	public void setTask_answer(String task_answer) {
		this.task_answer = task_answer;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	
}
