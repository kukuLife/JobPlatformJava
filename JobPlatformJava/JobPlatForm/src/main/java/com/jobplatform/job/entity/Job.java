package com.jobplatform.job.entity;

public class Job {

	public String jobId;
	public String jobName;
	public String jobInfo;
	public String jobKeyWord;
	public String startDate;
	public String endDate;
	public String createDateTime;
	public String updateDateTime;
	
	public String getJobId() {
		return jobId;
	}
	public void setJobId(String jobId) {
		this.jobId = jobId;
	}
	public String getJobName() {
		return jobName;
	}
	public void setJobName(String jobName) {
		this.jobName = jobName;
	}
	public String getJobInfo() {
		return jobInfo;
	}
	public void setJobInfo(String jobInfo) {
		this.jobInfo = jobInfo;
	}
	public String getJobKeyWord() {
		return jobKeyWord;
	}
	public void setJobKeyWord(String jobKeyWord) {
		this.jobKeyWord = jobKeyWord;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getCreateDateTime() {
		return createDateTime;
	}
	public void setCreateDateTime(String createDateTime) {
		this.createDateTime = createDateTime;
	}
	public String getUpdateDateTime() {
		return updateDateTime;
	}
	public void setUpdateDate(String updateDateTime) {
		this.updateDateTime = updateDateTime;
	}
	
	
}
